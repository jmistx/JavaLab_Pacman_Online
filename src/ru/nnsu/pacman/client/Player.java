package ru.nnsu.pacman.client;

class Player {
    private int x;
    private int y;

    public Player() {
    }

    int getX() {
        return this.x;
    }

    int getY() {
        return this.y;
    }

    void setX(int x) {
        this.x = x;
    }

    void setY(int y) {
        this.y = y;
    }

}
